int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (nb);
	while ((i * i) <= nb && i < 46350)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
